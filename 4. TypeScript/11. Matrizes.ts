/* Declaração de arrays devem possuir a definição do tipo também: */

// Declarando array de números ;
let numbers: number[]
numbers = [1,2,3,4,5]

// Declarando array de strings:
let strings: string[]
strings = ['A', 'B', 'C', 'D']


/* Implicitamente */

// Declarando array de números implicitamente;
let numbersImplicit = [1,2,3,4]

// Declarando array de strings implicitamente;
